			「 做助教是一件光荣的事，既有兴趣使然，又深知其重任，我愿意将这种互帮互助的精神不断传承下去。」
		</p><br/>
		<p>来自2016级通信工程专业的张翰飞，对导论课有着特别的体验，因为他是作为一名转专业的同学进入信通院的。由于课业紧张，学院对他们在导论课上的要求不高，可以说是以体验为主。但是这短短一学期的导论课体验却对他影响很大，同学们学习的热情，做出的成果都让他印象深刻。</p><br/>



		<p><strong>“无论是在群里指导同学们完成实验的助教们，还是积极问着问题的同学们；亦或是在群里不断收集问题、改进教程的火花空间的团队成员，每个人都展现出了无与伦比的热情和干劲，而这份热情和干劲就是导论课最重要的精神内涵，它深深的打动了我。”</strong></p><br/>



		<p>如今，他成为了多校协作助教群中的一员，也因为积极活跃的优异表现，荣获了“好学长奖”。从课堂体验者到助教，张翰飞用自己的经历点亮星星之火，让“好学长”精神得以相传。</p><br/>
		<p>本期，我们来听听张翰飞的故事。</p>
