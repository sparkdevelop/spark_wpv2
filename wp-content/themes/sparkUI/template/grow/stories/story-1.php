		<h3>一门令人印象深刻的课</h3><br/>
		  <!-- 等着插入图片 -->

		
		<p class="hong">红领巾：你当时上导论课的感受是什么？</p>

		<p class="answer">
		<span class="name">张翰飞：</span>我是转专业的同学，相当于和下一届的同学一起上导论课。但是由于我们课程紧张，并没有按照要求完成导论课的全部内容，只是大体上体验了一下导论课。</p>

		<p class="answer">但是，在导论课群中和其他大一的同学聊天可以看得出他们对导论课的激情非常高，学习热情也很高昂。群里经常能够引起对一些知识点的讨论。<strong>整个导论课的知识量并不低于任何一门专业课，需要投入的时间也不少。但是，导论课的成就却远比别的专业课要高。毕竟是做出了一样东西，毕竟让自己清楚了以后自己做的事情的大体流程。更多的则是对自己的专业有了更深刻的理解，</strong>尤其是对于新生，许多词汇他们是陌生的，但经历过导论课，亲自体验了这些词汇背后的技术与实现，便有了新的认识。</p><br/>


		<h3>“ 一种自己的付出有了回报的自豪 ”</h3><br/>

		<p class="hong">红领巾：你做助教的契机是什么？</p>
		<p class="answer">
		<span class="name">张翰飞：</span>做助教是因为在上导论课的时候就积极在群里发言，帮助大家解决问题。此外，自认为自己在这方面是比较擅长的。虽说接触开源硬件的时间和大家都一样，做工程的经验也不比谁多。但是从小就有着对计算机、电路的爱好，以及曾学过的机器人和编程知识的功底，同时大一期间也创办了拉格朗日课程管理&直播平台，学习了PHP语言，凭借这些小优势，自认为还是可以胜任助教一职。最终成为一名助教也离不开参与火花空间创客社团，通过社长的推荐，成功入职助教。</p>

		<p class="hong">红领巾：成为助教后有什么新的感受和体会吗？</p>
		<p class="answer">
		<span class="name">张翰飞：</span>一是很<strong>开心，</strong>帮助了其他同学，解决了他们的困惑，他们的一句句谢谢让我倍感欣慰。</p>

		<p class="answer">二是也锻炼了自己，不仅仅是自己的<strong>专业素养，</strong>也是自己的<strong>人际沟通能力，</strong>解决问题的过程大部分涉及到的是与同学的交流，交流的有效的程度直接影响了最后的收效，和不同的人交流问题，让我丰富了和他人沟通的技巧。</p>

		<p class="answer">三是让自己<strong>发现了自己的不足之处，</strong>毕竟只有把知识用起来，才能检验自己对知识的掌握。给别人解答问题其实也是自己在做练习。这样巩固了已有知识，也发现了自己知识上的不足。</p>

		<p class="answer">最后，就是一种<strong>成就感，</strong>看到大家最终能取得成果，而且离不开我给予他们的帮助，这是一种自豪，一种自己的付出有了回报的自豪。</p>

		<img src="<?php bloginfo("template_url") ?>/img/grow/stories/zhf/张翰飞2.jpg">
		<p><br/></p>
     <!-- 等着插入图片 -->


		<p class="hong">红领巾：你觉得在助教工作中，最大的收获和印象最深刻的事情是什么？</p>

		<p class="answer">
		<span class="name">张翰飞：</span>最大的收获就是<strong>锻炼了人际沟通能力吧。</strong>有一次和一位同学交流他遇到的问题，可能是由于我们专业的区别以及他的年级所限，一些词汇他并不能理解。我们的沟通一度陷入停滞，彼此都不能说明白。他描述不出来他遇到的问题，我也没办法用他理解的语言说出他应该做的事情。</p>

		<p class="answer">作为一名助教，就要既能从学生的角度出发，把大问题化小问题，又要有着老师对知识的理解与掌握。于是，我带他从最基础的实验开始，不断向其中加入新的成分，解释新的名词、意义，用这种逐级递进的方式，把他遇到的问题给他解释清楚了，又解决了问题，他也在这之间学习到了这部分知识。这便是让我理解，<strong>一定要站在问问题的同学的角度上思考问题，换位思考，增加交流沟通的有效程度。</strong></p><br/>


		<h3>“ 为后三年的学习打下了牢靠的基础 ”</h3><br/>
		<p class="hong">红领巾：你觉得导论课的这种上课形式对你的大学培养有什么影响和改变吗？</p>

		<p class="answer">
		<span class="name">张翰飞：</span>其实在我没转专业之前，我就听说了信通的这种导论课的形式，我就很好奇。一门课，带着大家一边做东西一边学习，最后通过自己的创造力，完成一个小的工程，最后还可以参加导论成果展。这样的学习进程比那种在教室中沉闷的讲课不知道要好多少倍。也只有这样亲自动手，才能有最深刻的理解。</p>

		<p class="answer">同时，导论课也是个<strong>培养团队精神的课程，</strong>一个人做的东西肯定不如团队合作的。团队合作又涉及到如何分配团队中的工作，如何求同存异。这些重要的团队合作经验，开发经验，我相信是对未来在公司中工作时大有裨益的。</p>


		<p class="answer"><strong>这种富于动手富于实践的培养方式，改变了大学课程的枯燥和难以理解。</strong></p>
		<p class="answer">最后这门课拿出来的成就也远比考试成绩要有意义的多，也是一种对大家学习的激励。更重要的是，导论课是一个引子，是一个在一开学就放眼整个大学学习的一个预览，及时让大家明确了方向，找准了目标。</p>
		<p class="answer">同时，导论课也培养了自学能力，培养了阅读文档的能力，培养了查阅资料的能力，培养了迁移的能力，这些最基本的能力在以往的学习中是需要自己悟出来的，但是有的人可能很晚才懂，导致之前的学习效率很低。通过导论课，一个学期的时间，集中培养，为后面三年的学习打下了牢靠的学习能力的基础。</p>

		<img src="<?php bloginfo("template_url") ?>/img/grow/stories/zhf/张翰飞3.jpg"><p><br/></p>

		<h3>关于“拉格朗日”</h3><br/>
		<p class="hong">红领巾：上面说到你有一个工作室叫拉格朗日，这个工作室主要是做什么事呢？</p>
		<p class="answer">
		<span class="name">张翰飞：</span>拉格朗日是受闫浩老师委托，我们几位同学一同建立的课程管理与直播的平台。主要是对课程资源进行管理，拥有一些如课件分发，作业提交等常用的辅助功能。但是我们也融入了直播，把优秀的课堂直播出去，辅以课程管理，形成一个融入直播能力的“慕课+”的平台。</p>

		<img src="<?php bloginfo("template_url") ?>/img/grow/stories/zhf/张翰飞4.jpg">
		<p><br/></p>


		<p class="hong">红领巾：成立这个工作室的初衷是什么，平时如何运营它？</p>
		<p class="answer">
		<span class="name">张翰飞：</span>成立之初衷是给予所有人收看好的课程的地方。直播+课程管理的模式可以督促用户的学习。同时在这其中也可以产生“学习”大数据，可以分析用户的学习特征，学习属性，适当的推出个性化辅导措施等。</p>

		<p class="answer">运营很简单，主要是资料的收集与整理，以及直播到场进行布置与设备的架设。现在我们已经发展到20余人的团队，除了正常的直播课程以外，还在运营答疑、付费知识、公众号运营。</p>
		<p class="answer">此外我们也拓展了新的业务，我们深受导论课的启发，试图发扬导论课精神，纵向发展，让导论课的理念提前进入学生的眼中。同时我们也希望能够借火花空间中的“项目”大数据，汇总设计新的课程。推广到低于大学以外，作为一种素质教育的形式，推至低年级。</p>
		<p class="answer">同时我们积极推进其他科目融合为一体的多学科交叉的课程，以导论课形式，展开对多种学科的预备式、体验式教育，提升综合素质能力。</p>

		<img src="<?php bloginfo("template_url") ?>/img/grow/stories/zhf/张翰飞5.jpg">
		<p><br/></p>



		<h3>与想象中不一样的大学生活</h3>
		<br/>
		<p class="hong">红领巾：如今你的大学生活已经过半，回顾这两年，和你当初想象中的大学生活有什么差别吗？</p>
		<p class="answer">
		<span class="name">张翰飞：</span>说实话，现在的大学生活比当初想象的累，事情要多，学业要更紧张。在一定程度上，生活比高中还要索然无味了。</p>
		<p class="answer">但是，也有导论课这样让人眼前一亮的课程，这样的全新体验，感觉大学生活还是很充实的。此外，大学中由于班的概念的淡化，班内同学彼此帮助的并不如高中的多。这种互帮互助的精神的确要少了不少。但是，<strong>导论课中的助教均是由高一级到两级的学长学姐亲自来辅导，又让这种传帮带，互帮互助的氛围浓厚了起来，一同学习一同进步的氛围也很浓厚。</strong>这还是有一些让我出乎意料的。</p><br/>


		<h3>助教寄语</h3><br/>
		<p class="hong">红领巾：最后，有什么寄语想对未来想当助教的学弟学妹说吗？</p>
		<p class="answer">
		<span class="name">张翰飞：</span>来吧，在这里可以学到更多！在这里，帮助别人的喜悦是不能比拟的。我们等你一起来！</p><br/>





