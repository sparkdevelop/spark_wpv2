		<h3>NO.1好学长</h3>
		<img src="<?php bloginfo("template_url") ?>/img/grow/stories/yzb/1.jpg">
		
		<p class="hong">红领巾：首先祝贺你获得了好学长奖！能不能谈一谈从导论课助教到好学长的经历与收获呢？</p>

		<p class="answer">
			<span class="name">于子斌：</span><strong>完成大一的导论课后，我在大二主动成为了导论课的助教。我觉得是“薪火相传”的精神激励着我努力做一位好助教。</strong></p>

		<p class="answer">讲个小故事：我刚刚上大学报到的时候，几个学长请我吃了饭，我坚持要自己付钱的时候，他们跟我说：“等你的学弟学妹来找你玩的时候，你请他们吃饭就可以啦！”这件事情让我很感动，这是我<strong>第一次感觉到来自学长学姐的温暖。</strong></p>

		<p class="answer">后来在导论课上接触到了火花空间的学长学姐们，他们给予了我很多帮助，耐心又细心，让<strong>我的大一生活温暖又好玩。我当时暗下决心：虽然可能暂时没办法报答学长学姐们对我的指导，但我一定要把这样的火焰传下去，好好帮助我的学弟学妹，就像学长学姐帮助我一样。</strong></p>

		<p class="answer">成为助教一方面使我扎实了基本功，另一方面也培养了我与他人沟通的能力，例如我需要给学弟学妹们发放器材、讲解知识，出验收题目、组织验收等等。更重要的是通过助教这个身份获得了内心的充实与满足，我觉得自己帮到了下一届学生，我的大二生活因此更有意义</p>

		<p class="hong">红领巾：你认为成为导论课的助教团队一员需要哪些品质呢？</p>

		<p class="answer"><span class="name">于子斌：</span>一方面是乐于分享，助人为乐，不求回报的品质。我大概是凭着一腔热血，只想着要帮助新生，并没有想过物质奖励什么的。另一方面就是技术过关，调bug的经验尽量丰富吧。大概像「你遇到的bug我都遇到过」这一类型会比较容易胜任（哈哈哈）。</p>
		<p><br/></p>


		<h3>NO.2他曾是萌新</h3>
		<img src="<?php bloginfo("template_url") ?>/img/grow/stories/yzb/2.jpg">
		<p><br/></p>
		<p class="hong">红领巾：你是什么时候接触导论课的？第一次听说时是如何看待这门课程的？</p>

		<p class="answer"><span class="name">于子斌：</span>刚刚接触导论课是2016年大一上学期，那时候还是个小白。听说有这门课程之后我马上去咨询了学长，得出“导论课很水，只需要写论文”这样的结论。没想到我们这届导论课大变样，一个装着实验器材的小盒子给我们的课程带来了翻天覆地的变化。</p>

		<img src="<?php bloginfo("template_url") ?>/img/grow/stories/yzb/3.jpg">
		<p><br/></p>
		<p class="answer">课程一开始，我自己动手的兴趣就被激发了。从做LED呼吸灯实验入手，在答疑老师的指导下用面包板连接好第一个电路，到最后能一点一点调好一个大项目，整个过程对大一的我而言，<strong>仿佛开天辟地一般打开了新世界的大门，让我对自己的专业有了不一样的认识。</strong>这是一门真正让我从一无所知到入门动手的课程，并且让我有了初步的工程认知概念，对于大一新生特别是以前没接触过工程项目的小白来说简直帮助太大了。</p>

		<p class="answer">它与以前学过的所有课程都不一样：以前的课程是学会一个知识点，然后在题目中去练习应用；导论课则是遇到了一个问题，然后自己摸索着解决，真正的“学中做”，我需要考虑更多实际问题。</p>

		<p class="answer">在学习过程中，<strong>我印象最深的就是团队协作&学长帮助</strong>——其余课程自己只能自己去学习理解，而导论课需要一个team共同努力，为一个目标而奋斗。助教剡恺学长帮助了我很多，没有他真的要走好多弯路。</p>

		<P class="hong">红领巾：谈谈你在导论课上的收获吧！</P>

		<p class="answer"><span class="name">于子斌：</span>导论课给我带来的收获真的太多！我认识了超级nice的老师，比如老纪（这里叫纪老师“老纪”，因为他给我的感觉像是一位年长的大朋友，非常亲切）、吴振宇老师、孙文生老师。我认识了许多外班的超级大佬&<strong>志同道合的小伙伴，</strong>比如李博阳、梅婷、苏子扬还有辜寂和梦婕小姐姐（朱梦婕）。</p>

		<p class="answer"><strong>正因为有了这些小伙伴的支持和帮助，我们能成立火花创客社团，一起“真刀真枪，做点东西”。我的能力得到了锻炼，特别是面对自己从未接触过的领域的问题搜索和解决能力。</strong></p>

		<p class="hong">红领巾：那你身边的同学怎么看待导论课呢？</p>

		<p class="answer"><span class="name">于子斌：</span>无论什么样的课程总有不同的声音出现，导论课也是这样。我身边的同学评价是褒贬不一：许多同学和我一样在课程中享受，而有的同学认为这个课程只是简单地复制代码&应付验收。</p>
		<p class="answer">我觉得这是我作为助教下一阶段需要重点攻克的内容：<strong>如何解决大一新生中的“咸鱼”问题？</strong>只有这样，才能让更多的同学有好的学习体验。理解代码的作用而不是直接复制粘贴；理解专有名词的含义而不是把课程水过去；理解知识，形成清晰的知识图谱而不是简单应付验收。</p>
		<p><br/></p>


		<h3> NO.3火花社团社长</h3>

		<img src="<?php bloginfo("template_url") ?>/img/grow/stories/yzb/4.jpg">
		<p><br/></p>
		<p class="hong">红领巾：大家所知道的火花社团是一个创客社团，作为火花社团的现任社长，可以再详细介绍一下它吗？</p>
		<p class="answer"><span class="name">于子斌：</span>大一新生火花创客社团成立的契机是在宏福校区举办的一场hackthon。一开始没有意识到要去学校的社团联合会注册，可以说是一个“野团”了，但是社员们都玩儿得超级开心。到了大二，社团工作步入正轨，觉得自己身上多了份责任。</p>
		<p class="answer">我对社团的定位是：<strong>创造力，活力，享受过程，开心。</strong>最后一点，“开心”，是我在上了老纪两个学期的互联网产业与创业课程之后才领会到的，开心是我们在学习过程中最需要的东西。希望在社团活动办得更好的基础上，吸引更多志同道合者加入，<strong>不仅是我们的社团成员玩儿得开心，所有参加社团活动的同学都能在开心中有所收获。</strong></p>
		<img src="<?php bloginfo("template_url") ?>/img/grow/stories/yzb/5.jpg">
		<p><br/></p>
		<p class="answer">正是因为追求开心，社团的活动类型从上学期的<strong>「硬件焊接教学」、「编程语言教学」</strong>变成了现在的<strong>「当创客遇上即兴剧」、「火花空间童年日巡礼」</strong>。比起教会大家焊接技巧 or 某种编程语言 or 某种算法，我更愿意让大家在学习知识的同时变得更加开心，更有活力。<strong>感觉我自身在和社团一起迅速成长！</strong></p>
		<p class="hong">红领巾：最后请你谈谈对社团的未来展望吧！</p>
		<p class="answer"><span class="name">于子斌：</span>最现实的目标：成为全校的“五佳社团”。上学期社团的评分在所有社团中排名第八，希望这学期能有所进步，进入前五。</p>
		<p class="answer">稍微远大一点的目标：自己拉赞助，举办大型的创客活动。我们现在的课程受众太少，虽然之前想过直播的方式，但是我最希望我们能有足够的经费支撑一些全校性的活动落地。希望更多人参与到我们的社团活动和建设中来。</p>
		<p class="answer">下学期目标：在导论课的推广中起到更大作用。<strong>现在北邮的新生工程教育在做多校模式的推广和探索，我希望社团能在这个过程中发挥自己的一份力。不仅仅是北邮的学生，我希望全国的学生都能参加创客活动，在开心中获得收获，丰富他们的大学生活。</strong></p>
		<p><br/></p>


