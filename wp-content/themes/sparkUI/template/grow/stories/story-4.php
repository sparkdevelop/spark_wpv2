<h3>Q&A</h3><br/>
<p class="hong">红领巾：宿舍组队或团队协作的感受是什么？</p>
<p class="answer">宿舍组队的话，我们会更方便地进行讨论。</p>


<p class="hong">红领巾：大家都会利用什么时间做这些东西，平时在寝室都会研究吗？</p>

<p class="answer">当然会做研究，主要是兴趣。</p>


<p class="hong">红领巾：当初怎样想到做多功能环境监测车的？</p>
<p class="answer">我们这边主要是大一上学期在通信导论课上学了Arduino，于是就想课下自己做些小创意。比如，我们宿舍没有温度计，我们就我们就自己上网买了个Arduino温度测试模块，自己写代码，做出个宿舍温度计来。像这个信通导论展的创意也是这样，任何人都不可能一下想到非常好的创意，但是呢，我看过以前TED上有一个乐队关于创意的演讲，他们说得就特别好，“<strong>我们有可能一下想不出特别独特的创意，我们一开始只能想到一些很普通很普通的东西，我们很有可能想到100个普通的东西，想到0个创意，但是，如果我们从这100个普通的东西中提取出一些东西，随意组合，那或许组合出来的就是一个不错的创意。”</strong></p>
	<p class="answer">我们一开始根本没想到环境监测车，就是想做个遥控车自己宿舍里玩，然后呢，我们又想到做宿舍的温湿度监测装置，于是就想到把环境监测的模块，比如火焰传感器，温度传感器加在小车上，然后为了小车不撞到墙，我们又加了超声波测距和红外遥控，这个时候我们的创意就快想到了，我们想，这个车能监测环境，那干嘛不把数据发到电脑上手机数据呢？于是开始收集数据。</p>
		<p class="answer">收集完数据，感觉又没什么用，然后我们突然想到可以让车持续监测一个范围内的环境数据。这能用在哪呢？可以用在需要长期监管的工厂。那怎么让车自行监测工厂的各个位置呢？于是我们又添加了灰度寻迹功能。</p>
			<p class="answer">最后，我们环境监测车的创意就这样完成了。</p>
<p class="hong">红领巾：过程中有发生什么你觉得好玩的事情吗？</p>

<p class="answer">有时候会遇到比较有意思的问题，比如超声波测距，会测出报表的距离，然后温湿度显示0，寻迹寻着寻着小车就出去了，反正遇到的问题都挺有意思的。不过，<strong>能解决问题才是真的爽。</strong></p>
<p class="hong">红领巾：遇到困难，都怎么解决的呢？</p>
<p class="answer">俗话说的好，知之为知之，不知百度知，出问题了，就百度，看百度上面人家遇到类似的问题怎么解决的，然后再自己想办法。</p>








<h3>关于设计</h3><br/>
<div class="box1">
	
	<p class="answer"><span class="name">庞旭：</span></p>
	<p class="answer">其实就是弄明白每一个部分的原理，自然就能想到。比如我们自制的电机模块和I2C通信，就是从最先在导论课上学的时候弄明白三极管的原理及其用法——即单管放大元件。实际上，在没有学过模电的前提下，我也是后来才知道三极管是单管放大元件的。一开始，通过看导论课的电路图时，发现三级管就像个开关，只不过是通过电流控制开关的，而不是人工。</p>
	<p class="answer">这就是最初我获得的信息，这也是一个智力正常的人能获得的简单信息。然后就是电机，向电机输入不同方向的电流，电机转的方向也不同，这也是大家都知道的。那么现在关键的地方来了，那就是如何把已知的信息整合起来，想到电机可以连接两条电流方向相反的电路，通过开关控制哪条电路接通，哪条中断。而开关呢，就用三极管来充当就行了。</p>
	<p class="answer">也许你会感觉我上面的想法很独特，但其实一点都不。只是，在这个思路中，如果任意一个原理没有弄懂，那就是想不出来。所以，<strong>研究要从原理入手。</strong></p>
	
	<p class="answer">当知道直流电机运作时，红外遥控接收会出问题，那就想到用已学的I2C通信解决问题。关键还是你得知道怎么实现I2C通信。</p>
	<p class="answer"><strong>与其说设计能力，不如说通过自学解决问题的能力，还有就是尽量自己尝试，尽量不走寻常的老路，</strong>当时onenet传输有网络延迟，那就用本地服务器……服务器接收不到数据？那就用wireshark抓包，检查是否成功进行了三次握手……</p>
	<p class="answer"><strong>把自己的作品的bug一点点修复，我们只是把许多个百分百能做出的功能拼凑在了一起，然后就做出来了。</strong></p>
	<p class="answer">而设计创意也就是这么弄出来的。</p>
	<p><br/></p>
</div>
<p><br/></p>

<div class="box1">
	
	<p class="answer"><span class="name">钱骁伟：</span></p>

	<p class="answer">一开始，我们想尽量综合导论课所学，做出一些好玩的东西，于是有了小车的雏形。后来，我们考虑到了它的实用性，于是我们又针对项目需求增设功能并且调整小车的框架模型。最后，我们考虑到自身能力和用户体验，自建了本地服务器并设计了相应网页，使项目真正具有一定的实用价值。</p>
	<p><br/></p>

</div>
<p><br/></p>
<div class="box1">
	
	<p class="answer"><span class="name">单兴中：</span></p>

	<p class="answer">我们组的展品小车的原型，其实是基于庞旭同学对三极管的兴趣和对电机驱动的好奇所设计的自制电机组装而成的一个简易的小车。</p>
		<p class="answer">但是当我们四人决定以这个为基础作为我们的导论课作业时，我们便开始思考，这个小车，连同我们在导论课上学到的诸如传感器、Wi-Fi通信、OLED屏幕、LED彩灯的各种部件，组合在一起，会产生怎样的产品？<strong>这是在选修课上养成的思考模式，即，将本次结课作业当作一个产品来做。</strong></p>
			<p class="answer"><strong>作为一个产品，必须要有目标客户，要有实际的应用场景。</strong>于是在我们内部讨论后，决定<strong>综合循迹避障、火焰探测、温湿度探测、数据实时传输、红外遥控等技术，为物流仓储场景提供一个可供警报、监测功能的多功能小车。</strong>其实最终的功能总和，也是在讨论中慢慢完善的。</p>
