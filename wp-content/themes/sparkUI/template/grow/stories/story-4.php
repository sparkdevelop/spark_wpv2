<p class="answer"><span class="name">单兴中：</span></p>



<p class="answer">这次导论课创新展是一个很好的机会，第一天观展的，有很多大牛。他们在听讲解过程中也会提出许多疑问。比如我们引以为傲的自制电机和所谓的“差异性”，其实只是相较于本次参展的其他同学而言，如果将范围放大，我们做的东西，其他任何人也能想到、也能做出来。这些都让我印象深刻，<strong>也会让我受益终身：以后做项目或者自行创业时，我不同于其他人的点在哪里？怎样才能做出特色？怎样才能吸引用户选择我的产品，而不是其他同行业竞争者？我的核心竞争力在哪里？这些都是参展前所不曾考虑的事情。</strong></p>



