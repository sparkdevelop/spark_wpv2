			<h3>胡彤感悟（二）</h3>
			<h4>关键词：导论_最后的总结</h4>
			<p>从周三中午报名参加比赛，晚上一起写代码到一点多；周四被Wifi模块和没有接口的Onenet搞到崩溃的人生第一次通宵；周五第一波专家、博导参与评分的展览以及晚上熬夜重新制作新的模型；再到今天上午第二波信息通信训练营参观……</p>
			<p>到现在，终于都结束了。</p>
    		<p>虽然导论课正儿八经的期末验收还没有进行，不过据说是线上验收，我们完善好参加创展的作品的创意、介绍、代码之类的资料就可以啦。</p>
    		<p>一学期的导论课，理论上我们都只是浅浅地了解了一点未来专业课要学的东西，可在创展的这里，真的深深体会到了即便是“导论”，还是有很多人借助着这一点点的引导，已经学到了大多人都无法想象的东西。</p>
			<p>都是靠自学的。</p>
    		<p>也让我明白了在自己为了一点小小的成就而满足时，还有很多很多人在以这点小成就为起点，开始真正深入探究更深更广的东西。知识永无止境。</p>
    		<p>传统的科目不会教给我这些。</p>
    		<p>只有当自己真正的在与前沿的知识与技术打交道时，才会明白到这种探索带来的价值。</p>
    		<p>也只有这样的接触，才会让自己时时刻刻体会到大学是不一样的。也许以前知道写代码是怎么回事，却也永远没有想过未来自己会用一行行敲出来的代码控制着硬件实现很多功能；没有想过自己会用电路实现很多很奇妙的事，没有想过自己会看到平日里使用的网站背后的复杂的世界；没有想过以前只是单纯用着一些软件，而现在自己也可以做出微信小程序、办一个公众号；没有想过自己亲手搭一个服务器，体会以前只有模糊概念的“云”……没有想到过的事很多很多。可如今它们真实的存在于自己的知识体系中，虽然很浅，但已经有了萌芽。</p>
    		<p>这个学习的过程，使我受益匪浅。</p>
    		<p>用开创了这门课的纪阳老师的话作为结尾：</p>
    		<p>“导论课与其他课不一样。它不是让大家一次学明白的，是让大家先有一次触摸，产生一个全局的概念，是为了让大家以后如果要做东西，可以慢慢跟着兴趣自学，也知道周边谁是大牛，应该怎么摸索的。其目的，不是成绩单，甚至也不是雏雁大创，是为了自己将来有时间感兴趣可以自己玩自己实验自己探索。</p>
			<p>导论课上一大堆实验提供了一个驱动自学的目标，你如果将来想作点东西做点事业，就得先把自学能力提升起来，才能在将来跟着自己的兴趣走。</p>
    		<p>昨天给其他学校分享我们大一新生这学期额目标是把小程序、智能硬件、网络、云等等全打通了，他们觉得难以想象。</p>
			<p>想到了，哪怕走几步，都是不一样的进步。爬过山之后，哪怕是坐缆车上去的，和始终在山下山外想象，肯定是不一样的。”</p>
