			<h3>程羽_20171223_感悟</h3>
			<h4>关键词：导论课创展</h4>
			<p>1）有的时候要学会逼自己，不逼迫自己无法激发自己的潜力。给自己设定一个很高的目标并为之努力，结果一定不会太差。如果你的目标是月亮，你至少也会身处群星。这次的导论课展很大程度上激发了我们的斗志、提高了自学的能力、学会了耐心解决各种难题，收获颇丰。</p>
			<p>2）深度学习。平时我对导论课的学习都是浅层次的，仅仅按照指导的步骤做出来最终结果并期盼着验收得A，但是重要的不是课程的结果而是我们学到的知识、被激发了兴趣之后的主动学习，我觉得这些我都没有达到，反而在昨晚独自探索微信小程序时感受到了快乐，开始了真正的深度学习，这才是导论课的最终目的吧（我要好好学习制作小程序了）。纪阳老师一直想让我们学得快乐、学得有趣，从而达到主动学习终身学习的目的，教育者用心良苦。</p>
			<p>3）珍惜自己的创意。今天和学长学姐们交流，他们做的都是实用性非常强的作品，比如防盗监测碰撞数据的智能快递盒、性价比高实用性强的积水测量仪、创新的无人机等等，相比之下我们更像是纯粹为了喜欢而做，有个学长说：“挺好的，等你们老了你们也会开始考虑实用性，现在好好畅想吧。”创意和实践改变世界，珍惜来之不易的创意。</p>
			<p>4）互相交流学习有助于进步。参展时我们混迹于各个小组的摊位，了解他们厉害的点子，全组克服的技术难关，知道了别人在学什么在做什么。我们小组也有个悬而未决的技术难题请教了很多同级的大佬和学长学姐们，纠结了一晚上的wifi模块是因为内存不够，硬件实在太坑了，好好学编程。交流中了解了很多比如灰度传感器、超声波测距、request命令、小程序应用等等知识，收获极大！！</p>
			<p>5）语言表达很重要，展现你们项目吸引人的部分才能留住参观者。</p>
			<p>6）学会赞扬别人并提出建议。每次看完一个展位我都会主动称赞有创意的点子或者技术的复杂性，根据我所能想到的信息结合其他小组的项目提出建议，然后设想产品的应用场景。</p>
			<p>7）团队合作很开心。211寝室公元一年共同度过的第一个大难关，通宵奋战到第二天下午没吃早午饭，但是大家的意志都在坚持着，合作很开心，我们是彼此的依靠。劫后余生被我列为人生一大乐事。</p>
