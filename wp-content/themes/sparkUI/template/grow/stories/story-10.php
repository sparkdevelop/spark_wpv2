			<h3>胡彤的感悟(一)<h3>
			<h4>关键词：创展第一天</h4>

     			<p>在经历了人生第一次通宵后，早晨身体全面崩盘，各种难受。然而支撑着用一早晨时间找到了昨晚一直做不好的部分的弥补方案、昨晚平均只睡了两个小时的宿舍四人下午强打起精神去参加创展时，还是深深地觉得人的潜力是无穷的。</p>
    			<p>早晨难受到根本站不起来，四个几乎没睡觉的人几乎都在崩溃边缘，然而在开始时间快到的时候却能给自己加油打气然后跑过去，一下午的时间站着精神抖擞地给来到展台前的专家、老师和同学讲解；原本觉得和许多大神相比起来很普通的作品，竟然被很多专家说做的非常棒非常有创意，还从他们的理解的角度给我们提了很多后续强化的建议，真的是受宠若惊。</p>
    			<p>感觉收获非常大。</p>
    			<p>用室友的话说，熬过了灾难般的昨晚后，今天终于在创展结束后松了一口气，感觉精疲力尽的同时，却又觉得特别开心，觉得世界特别美好。真的是这样。这是一次不一样的体验。</p>
    			<p>最后还有一些思考：</p>
    			<p>1.如果认真去看了看友情参展的大三学长学姐们的作品，就会发现大一和大三之间的区别还是很大的。不仅仅是代码写的比我们强很多、做出来的产品功能比我们强大许多，他们更注重产品的实际意义，更注重用户体验，看他们的作品时你会有一种诸如“如果这个东西未来能够推广并做到量产，一定会大大提高我们的生活质量／办事效率”的感受。不同于我们自己的作品娱乐价值高于实际应用价值，他们在创作时的思维和我们不同，这种思维的差异同时也反映出了一个人是重视眼前的娱乐还是有长远的思考规划，反映出了一个人创造社会价值的能力。我想这也是我欠缺的并需要持续改进的。</p>
    			<p>2.官方推荐你用的东西不一定就是最好的。只有当你多学习、多探索、多请教之后，才能找到最好的一种；</p>
    			<p>3.和专家交流是种很棒的体验。绝大多数的专家反而不是自己想象的那样严肃刻板、对我们这种水平做出来的东西不屑一顾的。相反，他们很平易近人，他们没有架子、会认真的听你的讲解并和你讨论想法，他们会以一种截然不同的思路来给你一些很棒的提议，他们会结合形势给你分析如何使你的作品更贴近需求、更符合时代潮流或者走在前沿、受众面更广……和他们交流几分钟的收获远远大于自己埋头苦干很久的所得。克服自己心里对于专家身份的惧怕感去自然地和他们互动，必受益无穷。</p>
    			<p>4.身边有很多非常优秀的同龄人，要打开自己狭隘的思想多向他们学习；</p>
    			<p>5.人的潜力是无穷的。你可以在很短的时间里爆发很强的能量学习大量的知识并完成看似不可能的任务。只要从心底相信并勇敢去做。</p>
    			<p>6.这么多年来，已学的东西其实少之又少，还有很多或实用或有趣的未知等待着我们去探索。学无止境，行无止境。</p>
