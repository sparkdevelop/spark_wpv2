	<p class="hong">红领巾：你是第一批助教吧，当时为什么要选择当助教呢？</p>
	<p class="answer">
			<span class="name">剡恺:</span>第一是因为有奉献精神吧。之前一直在做志愿者什么的，比较享受帮助他人的过程，有一定成就感。第二就是比较喜欢团队合作。</p>
		
	<p class="hong">红领巾：那么做助教的时候会不会有时候很累，想过要放弃呢？</p>
	<p class="answer">
			<span class="name">剡恺:</span>应该没有这种感觉，刚开始时候会觉得他们重复性的问题比较多，后来大家慢慢养成了自己搜索和上论坛提问的习惯，会的同学也会帮助其他同学，任务就轻松了很多。</p>


	<p class="hong">红领巾：嗯嗯，那么帮助同学们的过程中有没有什么有趣的事情或者让你印象深刻的事情？</p>
	<p class="answer">
			<span class="name">剡恺:</span>做助教认识了很多有趣的学弟学妹，现在也依旧有联系。大概我记得比较清楚的是一次去验收试验，中午有学妹加我QQ，说学长你还蛮帅的，然后下午一个宿舍的都来找我验收试验了。</p>


	<p class="hong">红领巾：然后作为助教，有没有哪些时候学弟学妹们问你问题把你问住了问蒙了的情况？</p>
	<p class="answer">
			<span class="name">剡恺:</span>刚开始问题都比较基础，后来大家都入门了就会有一些很神奇的问题，有时候所有操作都是对的但是结果不对，或者他们用一些我没有过的库和硬件，都很难给出解答。这时候我一般会去找找资料然后发给他们，一起学习……</p>
	<img src="<?php bloginfo("template_url") ?>/img/grow/stories/yk/yk2.jpg">
	<p class="hong">红领巾：嗯嗯，那最后一个问题，这些年的当助教有什么感想或者心情体会一类的？</p>
	<p class="answer">
			<span class="name">剡恺:</span>感受最深的就是现在新生的条件真好。现在互联网发展十分迅猛，而学校也能紧跟互联网发展节奏，第一时间向学生提供最新的软硬件平台，感觉这是北邮最突出的优点。而且北邮这种传帮带的氛围特别好，我自己也是在学长不断指导下学会很多东西，现在在实验室都是新东西，学长也会很耐心的指导我，希望这种氛围能一直持续下去。</p>
